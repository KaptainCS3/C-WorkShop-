 #include<iostream>
 using namespace std;

 int main(void){
    int a = 10, b = 4;
    int sum, sub, mul, div, mod;
    sum = a + b;    //! addition
    cout << sum << endl;
    sub = a - b;    //! subtration
    cout << sub << endl;
    mul = b * sub;  //! multiplication
    cout << mul << endl;
    div = sum / mul;    //! division
    cout << div << endl;
    mod = a % b;    //! modulus
    cout << mod << endl;

 }